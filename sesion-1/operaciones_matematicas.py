import math

# 1. Suma de productos
total_panes = 5 + 5
print(total_panes)

# 2. Resta de productos
precio_final = 25 - 5.50
print(precio_final)

# 3. Multiplicación de productos
precio_total = 5.50 * 3
print(precio_total)

# 4. División de productos
precio_unitario = 25 / 5
print(precio_unitario)

# 5. Combinación de operaciones
presupuesto = (total_panes * 5) * (precio_unitario + (precio_unitario * 0.18))
print(presupuesto)

# 6. Potenciación de productos
precio_con_descuento = 2 ** 5
print(precio_con_descuento)

# 7. Raiz cuadrada de productos
raiz_cuadrada = math.sqrt(25)
print(raiz_cuadrada)